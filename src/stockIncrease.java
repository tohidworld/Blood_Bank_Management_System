    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String bloodGroup=(String)jComboBox1.getSelectedItem();
        String unit=jTextField1.getText();
        String nullSelected="Select";
        String unitRegex = "^[0-9]*$"; 
        int unitVal=1;
        int intUnit=0;
        Pattern unitPattern = Pattern.compile(unitRegex); 
        Matcher unitMatcher = unitPattern.matcher(unit);
        if(unitMatcher.matches()){
            intUnit=Integer.parseInt(unit);
            unitVal=0;  
        }
        if(!bloodGroup.equals(nullSelected)){
            if(unitVal==0){
                try{
                    Connection con=ConnectionProvider.getCon();
                    Statement st=con.createStatement();
                    st.executeUpdate("update stock set units=units+'"+intUnit+"'where bloodGroup='"+bloodGroup+"'");
                    JOptionPane.showMessageDialog(null,"Successfully Updated");
                    setVisible(false);
                    new stockIncrease().setVisible(true);
                }
                catch (Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"Please enter valid units as Integer");
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select Blood Group");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

