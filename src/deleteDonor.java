    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String donorId=jTextField1.getText();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from donor where donorId='"+donorId+"'");
            if (rs.next())
            {  
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                SimpleDateFormat dFormat=new SimpleDateFormat("dd-MM-yyyy");
                jDateChooser1.setDate(dFormat.parse(rs.getString(5)));
                jTextField5.setText(rs.getString(6));
                jComboBox1.setSelectedItem(rs.getString(7));
                jTextField6.setText(rs.getString(8)); 
                jComboBox2.setSelectedItem(rs.getString(9));
                jTextField7.setText(rs.getString(10));
                jTextArea1.setText(rs.getString(11));
                jTextField1.setEditable(false);
                jTextField1.setBackground(Color.green);
            }
            else{
                JOptionPane.showMessageDialog(null,"Donor Id does not Exist");
            }    
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

